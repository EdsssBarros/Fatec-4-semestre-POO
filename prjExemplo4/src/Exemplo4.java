public class Exemplo4 {

    public static void main(String[] args) {
        int cont = 1;

        System.out.println("\t\t\tTabuada do 7\n");

        while (cont <= 10) {
            System.out.println("7 x " + cont + " = " + cont * 7);
            cont++;
        }
    }
}
